#include "commands.h"

int	ft_env(char **env)
{
	int	i;

	i = 0;
	if (!env)
		return (write(1, "\n", 1));
	while (env[i])
	{
		write(1, env[i], ft_strlen(env[i]));
		write(1, "\n", 1);
		i++;
	}
	return (0);
}
